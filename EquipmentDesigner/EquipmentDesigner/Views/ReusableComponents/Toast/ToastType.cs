namespace EquipmentDesigner.Controls
{
    /// <summary>
    /// Defines the visual type/severity of a toast message.
    /// </summary>
    public enum ToastType
    {
        /// <summary>
        /// Informational message (blue theme).
        /// </summary>
        Info,

        /// <summary>
        /// Success message (green theme).
        /// </summary>
        Success,

        /// <summary>
        /// Warning message (amber theme).
        /// </summary>
        Warning,

        /// <summary>
        /// Error/danger message (red theme).
        /// </summary>
        Error
    }
}
